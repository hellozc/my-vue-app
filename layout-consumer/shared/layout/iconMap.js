export function getIconChar(name, fallback = '•') {
  const trimmed = String(name || '').trim()
  if (!trimmed) return fallback
  if (iconCharMap[trimmed]) return iconCharMap[trimmed]

  const lower = trimmed.toLowerCase()
  for (const [keyword, char] of keywordFallbacks) {
    if (lower.includes(keyword)) return char
  }

  return fallback
}
